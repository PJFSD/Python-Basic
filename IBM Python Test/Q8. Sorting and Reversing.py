# Q8. Sorting and Reversing (5 Marks)
# Given the list scores = [88, 75, 92, 60]. Use two separate lines of code:

# Sort the list in place in ascending order.

# Reverse the newly sorted list in place so the highest score is the first element.
# Print the final list.

scores = [88,75,92,60]

# 1. Sort in ascending order (in place)
scores.sort()

# 2. Reverse the sorted list (in place)
scores.reverse()

print(scores)



