# Q6. List Slicing (5 Marks)

# Given the list alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h'], write a single slice operation that extracts the elements from index 3 up to, but not including, index 7. Store the result in a new list called sub_list.


alphabet = ['a','b','c','d','e','f','g','h']
# Slice from index 3 ('d') up to, but not including, index 7 ('g')
sub_list = alphabet[3:7]

print(sub_list)
